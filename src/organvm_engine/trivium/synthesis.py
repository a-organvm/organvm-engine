def _render_matrix_summary(
    matrix: dict[tuple[Dialect, Dialect], TranslationEvidence],
) -> str:
    """One-line summary of the matrix state."""
    total = len(matrix)
    with_evidence = sum(
        1 for ev in matrix.values() if ev.correspondences
    )
    total_corr = sum(
        len(ev.correspondences) for ev in matrix.values()
    )
    avg = (
        sum(ev.aggregate_strength for ev in matrix.values()) / total
        if total else 0.0
    )
    return (
        f"**{total} translation pairs** | "
        f"**{with_evidence} with empirical evidence** | "
        f"**{total_corr} total correspondences** | "
        f"**avg strength {avg:.2f}**"
    )


def _top_pairs(
    matrix: dict[tuple[Dialect, Dialect], TranslationEvidence],
    n: int = 5,
) -> list[TranslationEvidence]:
    """Return the N pairs with highest aggregate strength."""
    ranked = sorted(
        matrix.values(),
        key=lambda ev: ev.aggregate_strength,
        reverse=True,
    )
    return ranked[:n]


def _matrix_key(
    a: Dialect, b: Dialect,
    matrix: dict[tuple[Dialect, Dialect], TranslationEvidence],
) -> tuple[Dialect, Dialect]:
    """Find the correct key ordering for a pair in the matrix."""
    if (a, b) in matrix:
        return (a, b)
    if (b, a) in matrix:
        return (b, a)
    return (a, b)  # fallback
